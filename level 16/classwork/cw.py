# 1. შექმენით სია
names = ["ნიკა", "ანა", "გიორგი", "ლიზა", "თამარ"]

# 2. დაამატეთ ახალი ელემენტი სიის ბოლოს (ბოლო ელემენტის დამატება სიის გაწვდვის გზით)
# ახალი ელემენტის დამატება სიის ბოლოს
names = names + ["მია"]

# 3. წაშალეთ სიის 0 ინდექსზე მდებარე ელემენტი (ინდექსის გაწვდვით)
# სიის პირველი ელემენტის წაშლის მეთოდი: გაწვდეთ ახალი სია, სიის პირველი ელემენტის გამოტოვების გზით
names = names[1:]

# 4. დაბეჭდეთ განახლებული სია
print("განახლებული სია:")
for name in names:
    print(name)





# 1. შექმენით ცარიელი სია
car_names = []

# 2. მოიპოვეთ 5 მანქანის სახელი მომხმარებლისგან
print("გთხოვთ, შეიყვანოთ 5 მანქანის სახელი:")
for i in range(5):
    car_name = input(f"მანქანის სახელი {i+1}: ")
    # 3. ახალი სახელის დამატება სიის ბოლოს (append-ის გარეშე)
    car_names += [car_name]  # სიის გაწვდვა

# 4. დაბეჭდეთ სია
print("\nმანქანების სია:")
for name in car_names:
    print(name)
